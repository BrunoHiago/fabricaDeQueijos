    private void atualizarTabela(){
        PedidoBD conexao = new PedidoBD();
        ArrayList<Pedido> list = conexao.buscarPedido();
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        
        for(Pedido p:list){
            model.addRow(new Object[]{
                p.getId(),
                p.getCpf(),
                p.getData(),
                p.getEntrega()
            });
        }
    }
