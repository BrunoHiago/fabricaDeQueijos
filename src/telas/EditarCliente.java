    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        ClienteBD conexao = new ClienteBD();
        if(this.camposIsEmpty()){
            txtObs.setText("Possui Campo Vazios!");
        }else if(conexao.isExist(txtCPF.getText())){
            String nome = txtNome.getText();
            String telefone = txtTelefone.getText();
            String insta = txtInsta.getText();
            String face = txtFace.getText();
            String endereco = txtEndereco.getText();
            String email = txtEmail.getText();
            int cartao = Integer.parseInt(txtCartao.getText());

            Cliente c = new Cliente(txtCPF.getText(), nome, telefone, endereco,insta, email,face, cartao);

            conexao.editarCliente(c);

            this.dispose();
        }else{
            txtObs.setText("CPF nao Existe!");
        }
    }//GEN-LAST:event_btnEditActionPerformed
    
    private boolean camposIsEmpty(){
        return (txtCPF.getText().isEmpty()||
                txtNome.getText().isBlank() ||
                txtCartao.getText().isBlank() ||
                txtEmail.getText().isBlank() ||
                txtEndereco.getText().isBlank() ||
                txtInsta.getText().isBlank() ||
                txtFace.getText().isBlank() ||
                txtTelefone.getText().isBlank());
    }
