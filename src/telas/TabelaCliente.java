    private void atualizarTabela(){
        ClienteBD conexao = new ClienteBD();
        ArrayList<Cliente> list = conexao.buscarClientes();
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        
        for(Cliente c:list){
            model.addRow(new Object[]{
                c.getCpf(),
                c.getNome(),
                c.getTelefone(),
                c.getEndereco(),
                c.getInstagram(),
                c.getEmail(),
                c.getFacebook(),
                c.getCartaoCredito()
            });
        }
    }
